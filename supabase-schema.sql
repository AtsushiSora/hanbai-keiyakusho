  id text primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  buyer_name text,
  buyer_email text,
  vehicle_name text,
  total_price text,
  status text not null default '下書き',
  document_type text not null default '契約書',
  data jsonb not null default '{}'::jsonb,
