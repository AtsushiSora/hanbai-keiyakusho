create table if not exists public.order_auto_remote_contracts (
  id uuid primary key default gen_random_uuid(),
  contract_id text not null references public.order_auto_contracts(id) on delete cascade,
  owner_user_id uuid not null references auth.users(id) on delete cascade,
  access_token_hash bytea not null unique,
  passcode_hash text not null,
  contract_data jsonb not null,
  status text not null default '送信済み',
  failed_attempts integer not null default 0 check (failed_attempts >= 0),
  locked_until timestamptz,
  expires_at timestamptz not null,
  opened_at timestamptz,
  completed_at timestamptz,
  signer_name text,
  consent_items jsonb not null default '[]'::jsonb,
  signature_data_url text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.order_auto_remote_events (
  id bigint generated by default as identity primary key,
  remote_contract_id uuid not null references public.order_auto_remote_contracts(id) on delete cascade,
  owner_user_id uuid not null references auth.users(id) on delete cascade,
  event_type text not null,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

